/**
*   A TurtleLogger is an object that can be used to display a log of a Turtle's movements.
*
* @author: Stephan Jamieson
*
* @version: 1.0
* date: 12.08.2003
*
*/
public class TurtleLogger extends exploris.aquarium.TurtleLogger {

    /** 
     * Create a new TurtleLogger that will log the movements of "t".
     */
    public TurtleLogger(Turtle t) {
        super(t, 10, 20);
    }


    /**
     * Create a graphical display of the TurtleLogger on the computer screen.
     */
    public void display() {
        super.display();
    }
}

